/**
 * 获取备用图标
 * @param iconName 图标名称
 * @returns SVG字符串或null
 */
export function getFallbackIcon(iconName: string): string | null {
  // 清理图标名称
  const cleanName = iconName.toLowerCase().replace(/^icon-/, '').replace(/^#/, '')
  
  // 尝试多种匹配方式
  const possibleNames = [
    iconName,
    `icon-${cleanName}`,
    cleanName,
  ]
  
  for (const name of possibleNames) {
    if (FALLBACK_ICONS[name]) {
      return FALLBACK_ICONS[name]
    }
  }
  
  return null
}

/**
 * 检查是否有备用图标
 * @param iconName 图标名称
 * @returns 是否存在备用图标
 */
export function hasFallbackIcon(iconName: string): boolean {
  return getFallbackIcon(iconName) !== null
}

